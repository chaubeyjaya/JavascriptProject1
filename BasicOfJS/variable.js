var x = 10;
console.log(x);  // 10

var x = 20;      // re-declared (allowed)
console.log(x);  // 20

x = 30;          // updated (allowed)
console.log(x);  // 30


// let example
let y = 5;
console.log(y);  // 5

// let y = 10;   ❌ NOT allowed (re-declaration error)

y = 10;          // ✔ allowed (update)
console.log(y);  // 10


